    private void tblPermintaanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPermintaanMouseClicked
        int row = tblPermintaan.getSelectedRow();
        if (row != -1) {
            selectedPermintaanId = tblPermintaan.getValueAt(row, 0).toString();
            txtNama.setText(tblPermintaan.getValueAt(row, 3).toString());
            txtJumlah.setText(tblPermintaan.getValueAt(row, 1).toString());
            cmbStatus.setSelectedItem(tblPermintaan.getValueAt(row, 5).toString());

            try {
                Object val = tblPermintaan.getValueAt(row, 6);
                if (val != null && !val.toString().isBlank()) {
                    LocalDate parsed = java.sql.Date.valueOf(val.toString()).toLocalDate();
                    DateTglPanen.setDate(parsed);
                } else {
                    DateTglPanen.setDate(LocalDate.now());
                }
            } catch (Exception e) {
                System.err.println("Gagal parse tanggal: " + e.getMessage());
                DateTglPanen.setDate(LocalDate.now());
            }
        }    // TODO add your handling code here:
    }//GEN-LAST:event_tblPermintaanMouseClicked

