    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    boolean success = controller.tambahDistributor(
        txtNama.getText(),
        txtUsername.getText(),
        txtPassword.getText(),
        txtJumlah.getText(),
        txtNamaUsaha.getText(),
        txtWilayahPasar.getText()
    );

    if (success) {
        JOptionPane.showMessageDialog(this, "Data distributor berhasil ditambahkan!");
        loadTabelDistributor();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menambah distributor. Cek input atau username sudah terdaftar.");
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    int selectedRow = tblDistributor.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang mau diubah!");
        return;
    }
    String id = tblDistributor.getValueAt(selectedRow, 0).toString();
    String telp = txtJumlah.getText();
    String usaha = txtNamaUsaha.getText();
    String pasar = txtWilayahPasar.getText();

    if (controller.updateDataDistributor(id, telp, usaha, pasar)) {
        JOptionPane.showMessageDialog(this, "Data distributor diperbarui!");
        loadTabelDistributor();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal memperbarui data distributor!");
    }     
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    int selectedRow = tblDistributor.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang mau dihapus!");
        return;
    }
    String id = tblDistributor.getValueAt(selectedRow, 0).toString();

    if (controller.deleteDistributor(id)) {
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        loadTabelDistributor();
        clearForm();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
    } 
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblDistributorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDistributorMouseClicked
    int selectedRow = tblDistributor.getSelectedRow();
    if (selectedRow != -1) {
        txtNama.setText(tblDistributor.getValueAt(selectedRow, 1).toString());
        txtUsername.setText(tblDistributor.getValueAt(selectedRow, 2).toString());
        txtPassword.setText(tblDistributor.getValueAt(selectedRow, 3).toString());
        txtJumlah.setText(tblDistributor.getValueAt(selectedRow, 4).toString());
        txtNamaUsaha.setText(tblDistributor.getValueAt(selectedRow, 5).toString());
        txtWilayahPasar.setText(tblDistributor.getValueAt(selectedRow, 6).toString());
    }
    }//GEN-LAST:event_tblDistributorMouseClicked

