    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
       try {
        double jumlah = Double.parseDouble(txtJumlah.getText());
        String satuan = cmbSatuan.getSelectedItem().toString();

        boolean success = controller.tambahPanen(idUser, jumlah, satuan);

        if (success) {
            JOptionPane.showMessageDialog(this, "Data panen berhasil ditambahkan!");
            txtJumlah.setText("");
            loadTabelPanen();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah data panen!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah panen yang valid!", "Input Error", JOptionPane.WARNING_MESSAGE);
}

    }//GEN-LAST:event_btnTambahActionPerformed

