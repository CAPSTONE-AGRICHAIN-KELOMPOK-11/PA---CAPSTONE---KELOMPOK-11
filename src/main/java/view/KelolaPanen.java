    private void tblHasilPanenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHasilPanenMouseClicked
    int row = tblHasilPanen.getSelectedRow();
    if (row != -1) {
        selectedPanenId = tblHasilPanen.getValueAt(row, 0).toString();
        txtJumlahPanen.setText(tblHasilPanen.getValueAt(row, 1).toString());
        cmbSatuan.setSelectedItem(tblHasilPanen.getValueAt(row, 3).toString());
        selectedPetaniId = tblHasilPanen.getValueAt(row, 5).toString();

        try {
            Object val = tblHasilPanen.getValueAt(row, 4);
            if (val != null) {
                LocalDate parsed = java.sql.Date.valueOf(val.toString()).toLocalDate();
                DateTglPanen.setDate(parsed);
            } else {
                DateTglPanen.setDate(LocalDate.now());
            }
        } catch (Exception e) {
            System.err.println("Gagal parse tanggal: " + e.getMessage());
            DateTglPanen.setDate(LocalDate.now());
        }

        txtJumlahPanen.setEnabled(true);
    }
    }//GEN-LAST:event_tblHasilPanenMouseClicked

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    if (selectedPetaniId == null || selectedPetaniId.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Silakan pilih data petani terlebih dahulu sebelum menambah panen!",
            "Peringatan",
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        if (!txtJumlahPanen.isEnabled()) {
            JOptionPane.showMessageDialog(this,
                "Anda belum memilih data panen yang valid!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        double jumlah = Double.parseDouble(txtJumlahPanen.getText());
        String satuan = cmbSatuan.getSelectedItem().toString();
        LocalDate selectedDate = DateTglPanen.getDate();

        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal panen belum dipilih!");
            return;
        }

        boolean success = panenCtrl.tambahPanen(selectedPetaniId, jumlah, satuan);
        if (success) {
            JOptionPane.showMessageDialog(this, "Data panen berhasil ditambahkan!");
            loadData();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah data panen!");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!");
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    if (selectedPanenId == null) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    try {
        double jumlah = Double.parseDouble(txtJumlahPanen.getText());
        String satuan = cmbSatuan.getSelectedItem().toString();
        LocalDate selectedDate = DateTglPanen.getDate(); 

        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal panen belum dipilih!");
            return;
        }

        String idAdmin = Session.UserSession.getIdUser();
        if (idAdmin == null || idAdmin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sesi admin tidak ditemukan! Silakan login ulang.");
            return;
        }

        boolean success = panenCtrl.editPanen(selectedPanenId, (int) jumlah, satuan, selectedDate, idAdmin);

        if (success) {
            JOptionPane.showMessageDialog(this, "Data panen berhasil diperbarui!");
            loadData();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data!");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
      if (selectedPanenId == null) {
            JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this, "Yakin ingin menghapus data panen ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = panenCtrl.delete(selectedPanenId);
            if (success) {
                JOptionPane.showMessageDialog(this, "Data panen berhasil dihapus!");
                loadData();
                clearFields();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data panen!");
            }
        }       
    }//GEN-LAST:event_btnHapusActionPerformed

