    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tblPermintaan.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }

        String id = tblPermintaan.getValueAt(row, 0).toString();
        if (controller.deletePermintaan(id)) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            loadTabelPermintaan();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
        }     // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    try {
        int jumlah = Integer.parseInt(txtJumlah.getText());
        String status = cmbStatus.getSelectedItem().toString();

        Database.hasilPanenService panenSrv = new Database.hasilPanenService();
        String idPanen = panenSrv.getLastPanenId();
        if (idPanen == null) {
            JOptionPane.showMessageDialog(this, "Belum ada data hasil panen di database!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String idAdmin = UserSession.getIdUser();
        if (idAdmin == null || idAdmin.isBlank()) {
            JOptionPane.showMessageDialog(this, "Session admin tidak valid! Silakan login ulang.");
            return;
        }
      
        System.out.println("[DEBUG] ID Admin aktif: " + idAdmin);
        LocalDate selectedDate = DateTglPanen.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal belum dipilih!");
            return;
        }
        
        Model.Permintaan p = new Model.Permintaan();
        p.setIdPermintaan(null);
        p.setJumlahPermintaan(jumlah);
        p.setIdPanen(idPanen);
        p.setIdDistributor("DIS01"); 
        p.setIdAdmin(idAdmin);
        p.setTanggalPermintaan(java.sql.Date.valueOf(selectedDate));
        p.setStatus(status);

        if (controller.tambahPermintaan(p)) {
            JOptionPane.showMessageDialog(this, "Permintaan berhasil ditambahkan!");
            loadTabelPermintaan();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah permintaan!", "Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
      int row = tblPermintaan.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diubah!");
            return;
        }

        Permintaan p = new Permintaan();
        p.setIdPermintaan(tblPermintaan.getValueAt(row, 0).toString());
        p.setJumlahPermintaan(Integer.parseInt(txtJumlah.getText()));
        p.setIdPanen(tblPermintaan.getValueAt(row, 2).toString());
        p.setIdDistributor(tblPermintaan.getValueAt(row, 3).toString());
        String idAdmin = UserSession.getIdUser();
        if (idAdmin == null || idAdmin.isBlank()) {
            JOptionPane.showMessageDialog(this, "Session admin tidak valid! Silakan login ulang.");
            return;
        }
        p.setIdAdmin(idAdmin);
        p.setStatus(cmbStatus.getSelectedItem().toString());

    LocalDate selectedDate = DateTglPanen.getDate();
    if (selectedDate == null) {
        JOptionPane.showMessageDialog(this, "Tanggal belum dipilih!");
        return;
    }
    p.setTanggalPermintaan(java.sql.Date.valueOf(selectedDate));

        if (controller.updatePermintaan(p)) {
            JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
            loadTabelPermintaan();
            clearForm();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data!");
        }      // TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

