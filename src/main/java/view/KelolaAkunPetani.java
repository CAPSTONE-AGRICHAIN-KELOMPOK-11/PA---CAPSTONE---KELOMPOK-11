    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    Model.Petani p = new Model.Petani();
    p.setIdPetani(selectedId);
    p.setNamaLengkap(txtNama.getText());
    p.setUsername(txtUsername.getText());
    p.setPassword(txtPassword.getText());
    p.setNoTelp(txtNoTelp.getText());
    p.setNamaSawah(txtNamaSawah.getText());
    p.setLokasi(txtLokasi.getText());
    p.setLuasLahan(Integer.parseInt(txtLuasSawah.getText()));

    if (petaniCtrl.editPetani(p)) {
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
        loadData();
        tblPetani.revalidate();
        tblPetani.repaint();
        clearFields();
    } else {
        JOptionPane.showMessageDialog(this, "Gagal memperbarui data!");
    }    
    }//GEN-LAST:event_btnEditActionPerformed

