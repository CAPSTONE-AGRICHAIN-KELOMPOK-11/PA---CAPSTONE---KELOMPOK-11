    private void loadData() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID Petani");
        model.addColumn("Nama Lengkap");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("No. Telp");
        model.addColumn("Nama Sawah");
        model.addColumn("Lokasi");
        model.addColumn("Luas Lahan");

        for (Model.Petani p : petaniCtrl.getAllPetaniWithSawah()) {
            model.addRow(new Object[]{
                p.getIdPetani(),
                p.getNamaLengkap(),
                p.getUsername(),
                p.getPassword(),
                p.getNoTelp(),
                p.getNamaSawah() != null ? p.getNamaSawah() : "",
                p.getLokasi() != null ? p.getLokasi() : "",
                p.getLuasLahan()
            });
        }

        tblPetani.setModel(model);
    }
