    private void loadTabelPanen() {
            List<hasilPanen> daftar = controller.getPanenByPetani(idUser);

            DefaultTableModel model = new DefaultTableModel(
                new Object[]{"ID Panen", "Jumlah (Kg)", "Stok Gudang", "Tanggal Panen"}, 0
            );

            for (hasilPanen h : daftar) {
                model.addRow(new Object[]{
                    h.getIdPanen(),
                    h.getJumlahPanen(),
                    h.getStokGudang(),
                    h.getTanggalPanen()
                });
            }

            tblHasilPanen.setModel(model);
        }

