    private void loadTabelDistributor() {
        List<Distributor> list = controller.getAllDistributor();
        javax.swing.table.DefaultTableModel model = 
        (javax.swing.table.DefaultTableModel) tblDistributor.getModel();
        model.setRowCount(0);
        
        for (Distributor d : list) {
            model.addRow(new Object[]{
                d.getIdDistributor(),
                d.getNamaLengkap(),
                d.getUsername(),
                d.getPassword(),
                d.getNoTelp(),
                d.getNamaUsaha(),
                d.getWilayahPasar(),
                d.getRole()
            });
        }
    }

