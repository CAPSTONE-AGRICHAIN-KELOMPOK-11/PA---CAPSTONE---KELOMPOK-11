private void loadTablePermintaan() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Permintaan");
    model.addColumn("Nama Usaha");
    model.addColumn("No Telepon");
    model.addColumn("Wilayah Pasar");
    model.addColumn("Jumlah Permintaan (Kg)");
    model.addColumn("Status");
    model.addColumn("Tanggal Permintaan");

    List<Object[]> data = controller.permintaanForPetani();
    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");

    for (Object[] row : data) {
        String idPermintaan = (String) row[0];
        String nama = (String) row[1];
        String telp = (String) row[2];
        String wilayah = (String) row[3];
        Integer jumlah = (Integer) row[4];
        String status = (String) row[5];
        java.util.Date tgl = (java.util.Date) row[6];

        if (status != null && status.equalsIgnoreCase("dipenuhi")) {
            continue; 
        }
        model.addRow(new Object[]{idPermintaan, nama, telp, wilayah, jumlah, status, sdf.format(tgl)});
    }
    tblPermintaan.setModel(model);
}

    private void tampilkanStokGudang() {
        try (org.hibernate.Session session = Database.Koneksi.getInstance().getSession()) {
            session.clear(); 

            Database.hasilPanenService panenService = new Database.hasilPanenService();
            int stokTerbaru = panenService.getStokTerakhirByPetani(idUser);

            lblStok.setText(stokTerbaru + " Kg");
            System.out.println("[REAL-TIME] Stok terbaru user " + idUser + ": " + stokTerbaru + " Kg");

        } catch (Exception e) {
            e.printStackTrace();
            lblStok.setText("Gagal ambil data");
        }
    }

    private void setupTableClickListener() {
        tblPermintaan.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            int row = tblPermintaan.getSelectedRow();
            if (row >= 0) {
                String idPermintaan = tblPermintaan.getValueAt(row, 0).toString();
                String status = tblPermintaan.getValueAt(row, 2).toString();

                txtPermintan.setText(idPermintaan);
                cmbStatus.setSelectedItem(capitalize(status));
            }
        }
        });
    }
    
    private String capitalize(String text) {
        if (text == null || text.isEmpty()) return text;
        return text.substring(0, 1).toUpperCase() + text.substring(1).toLowerCase();
    }



