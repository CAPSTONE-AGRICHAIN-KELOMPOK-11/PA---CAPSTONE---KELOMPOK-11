    private void btnPerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaruiActionPerformed
        loadTablePermintaan();
        tampilkanStokGudang();
        try {
            Controller.hasilPanenController panenController = new Controller.hasilPanenController();
            Database.hasilPanenService panenService = new Database.hasilPanenService();

            int stokTerbaru = panenService.getStokTerakhirByPetani(idUser);

            java.util.List<Model.hasilPanen> daftarPanen = panenController.getPanenByPetani(idUser);

            if (!daftarPanen.isEmpty()) {
                Model.hasilPanen panenTerakhir = daftarPanen.get(daftarPanen.size() - 1);

                if (panenTerakhir.getStokGudang() != stokTerbaru) {
                    panenTerakhir.setStokGudang(stokTerbaru);
                    panenController.update(panenTerakhir);
                    System.out.println("[INFO] Sinkronisasi stok gudang: " + stokTerbaru + " Kg");
                }
            }
            JOptionPane.showMessageDialog(this, "Tabel dan stok gudang diperbarui!");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal memperbarui stok gudang: " + e.getMessage(),
                                          "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnPerbaruiActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String idPermintaan = txtPermintan.getText().trim();
    String statusBaru = cmbStatus.getSelectedItem().toString();

    if (idPermintaan.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Pilih permintaan yang ingin diubah terlebih dahulu!",
            "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    int selectedRow = tblPermintaan.getSelectedRow();
    if (selectedRow >= 0) {
        String statusSekarang = tblPermintaan.getValueAt(selectedRow, 5).toString();

        if (statusSekarang.equalsIgnoreCase("dipenuhi")) {
            JOptionPane.showMessageDialog(this,
                "Status tidak dapat diubah lagi!",
                "Akses Ditolak",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (statusSekarang.equalsIgnoreCase("disetujui") &&
            !statusBaru.equalsIgnoreCase("dipenuhi")) {

            JOptionPane.showMessageDialog(this,
                "Status tidak bisa diubah!",
                "Akses Ditolak",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
    }

    int confirm = JOptionPane.showConfirmDialog(
        this,
        "Apakah Anda yakin ingin mengubah status permintaan " + idPermintaan +
        " menjadi \"" + statusBaru + "\"?",
        "Konfirmasi Ubah Status",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.QUESTION_MESSAGE
    );

    if (confirm == JOptionPane.YES_OPTION) {
        boolean success = controller.ubahStatus(idPermintaan, statusBaru);

        if (success) {
            JOptionPane.showMessageDialog(this,
                "Status berhasil diperbarui!");
            loadTablePermintaan();
            tampilkanStokGudang();
        } else {
            JOptionPane.showMessageDialog(this,
                "Gagal memperbarui status!",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this,
            "Perubahan status dibatalkan.",
            "Dibatalkan", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

