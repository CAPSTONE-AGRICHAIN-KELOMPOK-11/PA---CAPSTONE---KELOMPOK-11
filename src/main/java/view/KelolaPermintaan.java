    private void loadTabelPermintaan() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID Permintaan");
        model.addColumn("Jumlah Permintaan");
        model.addColumn("ID Panen");
        model.addColumn("ID Distributor");
        model.addColumn("ID Admin");
        model.addColumn("Status");
        model.addColumn("Tanggal Permintaan");

        List<Permintaan> list = controller.getAllPermintaan();
        for (Permintaan p : list) {
            model.addRow(new Object[]{
                p.getIdPermintaan(),
                p.getJumlahPermintaan(),
                p.getIdPanen(),
                p.getIdDistributor(),
                p.getIdAdmin(),
                p.getStatus(),
                p.getTanggalPermintaan()
            });
        }
        tblPermintaan.setModel(model);
    }

