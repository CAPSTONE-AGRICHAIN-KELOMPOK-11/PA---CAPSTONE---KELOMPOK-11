private void loadData() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID Panen");
    model.addColumn("Jumlah Panen (Kg)");
    model.addColumn("Stok Gudang");
    model.addColumn("Satuan");
    model.addColumn("Tanggal Panen");
    model.addColumn("ID Petani");
    model.addColumn("ID Admin");

    for (Model.hasilPanen h : panenCtrl.getAllPanen()) {
        model.addRow(new Object[]{
            h.getIdPanen(),
            h.getJumlahPanen(),
            h.getStokGudang(),
            h.getSatuan(),
            h.getTanggalPanen(),
            h.getIdPetani(),
            h.getIdAdmin()
        });
    }
    tblHasilPanen.setModel(model);
    tblHasilPanen.revalidate();
    tblHasilPanen.repaint();

}

 
