    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    if (selectedId == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this, "Apakah yakin ingin menghapus data ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION
    );

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        if (petaniCtrl.delete(selectedId)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            loadData();
            clearFields();
        } else {
    JOptionPane.showMessageDialog(this, 
        "Tidak dapat menghapus data petani karena masih memiliki data hasil panen.",
        "Gagal Hapus", JOptionPane.WARNING_MESSAGE);
}
    }       
    }//GEN-LAST:event_btnHapusActionPerformed

