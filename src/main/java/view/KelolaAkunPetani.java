    private void btnTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambah1ActionPerformed
    Model.Petani p = new Model.Petani();
    p.setNamaLengkap(txtNama.getText());
    p.setUsername(txtUsername.getText());
    p.setPassword(txtPassword.getText());
    p.setNoTelp(txtNoTelp.getText());
    p.setNamaSawah(txtNamaSawah.getText());
    p.setLokasi(txtLokasi.getText());

    String luasStr = txtLuasSawah.getText().trim();
    int luasLahan;
    try {
        luasLahan = Integer.parseInt(luasStr);
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Luas sawah harus berupa angka!",
            "Input Tidak Valid",
            javax.swing.JOptionPane.ERROR_MESSAGE
        );
        return; 
    }

    p.setLuasLahan(luasLahan);

    if (p.getNamaLengkap().isEmpty() || p.getUsername().isEmpty() || 
        p.getPassword().isEmpty() || p.getNoTelp().isEmpty() || 
        p.getNamaSawah().isEmpty() || p.getLokasi().isEmpty()) {

        javax.swing.JOptionPane.showMessageDialog(
            this,
            "Semua field wajib diisi!",
            "Input Tidak Lengkap",
            javax.swing.JOptionPane.WARNING_MESSAGE
        );
        return;
    }

    if (petaniCtrl.tambahPetani(p)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        loadData();
        clearFields();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal menambah data!");
    }
    }//GEN-LAST:event_btnTambah1ActionPerformed

